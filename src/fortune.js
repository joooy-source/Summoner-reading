export function hashName(name) {
  let h = 0;
  for (let i = 0; i < name.length; i++) h = (h * 31 + name.charCodeAt(i)) >>> 0;
  return h;
}

// 해시에서 서로 다른 시드를 뽑아내기 (필드끼리 안 겹치게)
function seed(h, salt) {
  let x = (h ^ (salt * 2654435761)) >>> 0;
  x ^= x << 13; x >>>= 0;
  x ^= x >> 17;
  x ^= x << 5; x >>>= 0;
  return x >>> 0;
}
function pick(arr, h, salt) {
  return arr[seed(h, salt) % arr.length];
}

