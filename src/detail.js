// 챔프 칭호를 카드 제목용 pre/em으로 분리 (마지막 어절을 강조어로)
export function splitTag(tag) {
  const i = tag.lastIndexOf(" ");
  return i < 0 ? { pre: "", em: tag } : { pre: tag.slice(0, i), em: tag.slice(i + 1) };
}

