export function detailFor(r) {
  const base = r.nick || "";
  if (r.mode === "duo") {
    return [
      ["케미 분석", pick(DUO.케미, base, "k")],
      ["터지는 순간", pick(DUO.터지는, base, "t")],
      ["삐걱대는 순간", pick(DUO.삐걱, base, "s")],
      ["종합 평가", pick(DUO.종합, base, "v")],
    ];
  }
  if (r.mode === "fortune") {
    return [
      ["오늘의 흐름", pick(FORTUNE.흐름, base, "f")],
      ["조심할 것", pick(FORTUNE.주의, base, "w")],
      ["행운 포인트", pick(FORTUNE.행운, base, "l")],
    ];
  }
  return [
    ["플레이 성향", pick(SOLO.성향, base, "p")],
    ["강점", pick(SOLO.강점, base, "g")],
    ["주의할 점", pick(SOLO.주의, base, "c")],
    ["한 줄 처방", pick(SOLO.처방, base, "r")],
  ];
}
